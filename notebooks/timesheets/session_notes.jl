# ‚ïî‚ïê‚ï° c4116830-9bd3-11ee-1039-135c5ef7c31d
function write_summary(df)
	io = IOBuffer()
	indent = ""
	for  row ‚àà eachrow(df)
		write(io,
			"$(indent)[$(row.category)], [$(r2(row.hours))], [$(r2(row.rate))], [$(r2(row.pay))],\n"
		)
		indent = "\t\t\t"
	end
	stake!(io)
end

# ‚ïî‚ïê‚ï° c8e52fd2-3856-4ace-8bef-94161da14587
function write_log(df)
	io = IOBuffer()
	indent = ""
	for row ‚àà eachrow(df)
		write(io, "$(indent)[$(row.date)], [$(row.student_name)], [`$(row.notes)`],\n")
		indent = "\t"
	end
	stake!(io)
end

