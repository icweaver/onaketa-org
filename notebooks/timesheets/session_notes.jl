# ‚ïî‚ïê‚ï° 184bece7-c9d7-4c32-9fbf-be19221369c6
function generate_report(gdf, member, pay_date)
	df_log = gdf[(member, "Tutoring")]
	
	df_summary = @chain df_log begin
		groupby(:category)
		@combine :hours = sum(:hours)
		@rtransform begin
			:rate = rate(:category)
			:pay = rate(:category) * :hours
		end
	end
	
	report = report_src(df_summary, df_log, member, pay_date)
	
	mkpath("./src")
	name = replace(member, " "=>"")
	fname = "pay_summary_$(pay_date.year)_$(monthabbr(pay_date.month))_$(name)"
	spath = "src/$(fname).typ"
	write(spath, report)
	
	mkpath("./pdfs")
	ppath = "pdfs/$(fname).pdf"
	cmd = `typst compile --root . $(spath) $(ppath)`
	run(cmd)

	@debug "Report generated for $(member) $(pay_date)"
end

